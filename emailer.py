# ---------- Helper -----------------------------------------------------------

def clean(text: str) -> str:
    """
    â€¢ Replace nonâ€‘breaking spaces
    â€¢ Normalize fancy Unicode
    â€¢ Drop any char that canâ€™t roundâ€‘trip through UTFâ€‘8
    """
    if not text:
        return ""
    text = text.replace("\xa0", " ")
    text = unicodedata.normalize("NFKC", text)
    return text.encode("utf-8", "ignore").decode("utf-8")

